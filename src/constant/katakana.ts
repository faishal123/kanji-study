export const romajiToKatakana: { [key: string]: string } = Object.fromEntries(
  Object.entries(katakanaToRomaji).map(([katakana, romaji]) => [
    romaji,
    katakana,
  ])
);

export const readableKatakana = Object.keys(katakanaToRomaji).filter(
  (katakana) =>
    katakana !== "ッ" &&
    katakana !== "ャ" &&
    katakana !== "ュ" &&
    katakana !== "ョ" &&
    katakana !== "ー"
);
